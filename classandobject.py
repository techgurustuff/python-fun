# define a class
class Bike:
    name = "Road Bike"  # class attribute
    gear = 0

# create object of class
bike1 = Bike()

# access attributes and assign new values
bike1.gear = 5
bike1.name = "Mountain Bike"

print(f"Name: {bike1.name}, Gears: {bike1.gear} ")

bike2 = Bike()
# access attributes and assign new values


print(f"Name: {bike2.name}, Gears: {bike2.gear} ")
